<h2>Publications about Randoop</h2>

<p> These papers describe Randoop and its underlying techniques in detail.

<ul>
<li>
<b>&#8220;Feedback-directed random test generation&#8221;</b><br />
by Carlos Pacheco, Shuvendu K. Lahiri, Michael D. Ernst, and Thomas Ball.<br />
In <em>ICSE '07: Proceedings of the 29th International Conference on Software Engineering</em>, (Minneapolis, MN, USA), 2007.<br />

<p> The main paper describing feedback-directed random test generation
and Randoop, as well as experiments that compare the technique with
other test generation techniques.

  <br><br> <a
href="http://people.csail.mit.edu/cpacheco/publications/feedback-random-abstract.html">Click here for more details.</a>

<li>
<p>
<b>&#8220;Randoop: Feedback-directed Random Testing for Java&#8221;</b><br />
by Carlos Pacheco and Michael D. Ernst.<br />
In <em>OOPSLA 2007 Companion, Montreal, Canada</em>, Oct. 2007, ACM.<br />

<p> A short tool paper describing aspects of the Java version of Randoop.

  <br><br> <a
href="http://people.csail.mit.edu/cpacheco/publications/randoop_for_java-abstract.html">Click here for more details.</a>

<li> <b>&#8220;Finding Errors in .NET with Feedback-directed
Random Testing&#8221;</b><br /> by Carlos Pacheco, Shuvendu K. Lahiri,
and Thomas Ball.<br>
In <em>ISSTA 2008, Seattle, Washington</em>, Aug. 2008.<br/>
<p>

This case study details the use of Randoop at Microsoft (using a .NET
version of Randoop, developed internally at Microsoft). A team of test
engineers applied Randoop to a critical .NET component that had been
thorughly tested for years. Still, Randoop found many serious errors.
The case study appears in <em>ISSTA 2008: International Symposium on
Software Testing and Analysis</em>, (Seattle, Washington),
July&nbsp;20-24, 2008.

  <br><br> <a
href="http://people.csail.mit.edu/cpacheco/publications/randoop-case-study-abstract.html">Click here for more details.</a>

</ul>

